						<p>C'est bientôt l'été et comme beaucoup de gens vous serez pas mal à vouloir profiter de baignades dans divers lieux à l'hygiène souvent douteuse (<i>mer, lacs, piscines, spa échangistes etc…</i>) et du soleil ardant qui va avec.</p>
						<p>On revient donc sur deux points essentiels de la cicatrisation de votre tattoo : l'exposition au soleil et la baignade prolongée.</p><br> 
						<p><b>Il est très important de NE PAS s'exposer au soleil et d'éviter TOUTES les baignades prolongées.</b></p><br>
						<p>Le tatouage reste une plaie, parfois d'une taille assez importante, et comme pour toute plaie, le soleil et l'eau ne sont pas conseillés.</p>
						<p>Bien sûr une fois la période de cicatrisation terminée il est tout à fait possible de se baigner où on veut aussi longtemps qu'on veut.</p>
						<p>Pour le soleil il faudra cependant veiller à vous protéger à vie en utilisant de l'écran total (<u>indice 50</u>) afin de préserver au mieux votre tatouage et d'éviter son vieillissement prématuré (<i>traits qui s'épaississent, couleurs moins éclatantes etc…</i>).</p>
						<p>Voilà:) et si vous avez un doute ou si un truc bizarre apparaît le premier geste qui sauve c'est de <b>CONTACTER SON TATOUEUR</b>.</p>
						<p>À très bientôt !<p>
